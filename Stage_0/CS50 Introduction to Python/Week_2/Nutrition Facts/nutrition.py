def main():
    name = input("Item: ")
    if name == "apple":
        print("Calories: 130")
    elif name == "banana":
        print("Calories: 110")
    elif name == "Avocado":
        print("Calories: 50")
    elif name == "Sweet Cherries" or name == "pear":
        print("Calories: 100")
    elif name == "Kiwifruit":
        print("Calories: 90")


main()
